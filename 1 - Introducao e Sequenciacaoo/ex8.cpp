#include <iostream>
using namespace std;

int main()
{
    int a, b, m, s;

    cout << "Informe o 1 numero inteiro: ";
    cin >> a;
    cout << "Informe o 2 numero inteiro: ";
    cin >> b;

    m = a * 5;
    s = b + 10;

    cout << "1 numero multiplicado por 5: " << m << endl;
    cout << "2 numero somado com 10: " << s << endl;

    return 0;
}